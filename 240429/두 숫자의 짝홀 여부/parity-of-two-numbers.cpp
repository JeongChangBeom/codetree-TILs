#include <iostream>

using namespace std;

int main() {
    int a,b;
    cin >> a >> b;

    if(a%2 == 0){
        cout << "even" << '\n';
    }
    else{
        cout << "odd" << '\n';
    }

    if(b%2 == 0){
        cout << "even" << '\n';
    }
    else{
        cout << "odd" << '\n';
    }

    return 0;
}